package fr.projet.java.logiqueDuJeu;

/**
 * @author Romain Les action du joueur possible lorsqu'une unite est
 *         selectionner.
 */
public enum ActionUnite {

	/**
	 * Le joueur ameliore l'unite
	 */
	Ameliorer,
	/**
	 * Le joueur deplace l'unite
	 */
	Deplacer,
	/**
	 * Le joueur deselectionne l'unite.
	 */
	Deselectionner;

}
